            nombre_2 = p_nombre_2,
            apellido_1 = p_apellido_1,
            apellido_2 = p_apellido_2,
            fecha_nacimiento = p_fecha_nacimiento,
            telefono = p_telefono,
            email = p_email,            
            cod_sexo = p_cod_sexo,           
            cod_estado = p_cod_estado
        WHERE
            cod_persona = vl_secpersona;
   
   END IF;


 END VDIR_SP_GUARDAR_USUARIO;

